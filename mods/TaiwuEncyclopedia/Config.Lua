药老随身AI —— 你的太吾绘卷智能问答助手

[h2]一句话概括[/h2]
按 F8 呼出问答面板，向 AI 提问任何太吾绘卷相关问题。AI 会自动检索百晓册知识库（官方百科）、玩家攻略库和引导攻略骨架，基于真实资料生成回答，并标注信息来源。不是凭记忆瞎编——每个回答都有据可查。

[h2]它能做什么[/h2]

[b]门派功法查询[/b]：各门派特色、功法体系、BUILD 搭配思路，自动加载相关百晓册章节。
[b]战斗机制解读[/b]：伤害计算、破绽内息、毒素机制，查概念、查数值一步到位。
[b]攻略引导[/b]：剑冢攻略、开局选择、产业规划，内置 11 类引导骨架按需加载。
[b]玩家攻略查询[/b]：检索社区玩家贡献的攻略文档和经验分享。
[b]多轮追问[/b]：对话历史自动保存，支持连续追问和上下文理解。

[h2]怎么用[/h2]

[list=1]
[*]在游戏内按 [b]F8[/b] 打开问答面板。
[*]首次使用需点击面板右上角齿轮按钮进入设置，填写大模型 API：
[list]
[*][b]Base URL[/b]：API 地址（如 https://api.deepseek.com）
[*][b]API Key[/b]：你的 API 密钥
[*][b]Model[/b]：模型名称（如 deepseek-chat）
[/list]
[*]点「测试连接」验证配置，通过后「保存配置」。
[*]在输入框输入问题回车即可。AI 先检索资料，再流式输出回答。
[/list]

[h2]特色功能[/h2]

[b]渐进式资料加载[/b]：AI 先查章节概述，再按需加载详细条目，避免一次性塞入大量无关内容。
[b]百晓册全覆盖[/b]：涵盖全部门派、功法、战斗、产业、物品、NPC 互动等官方百科内容。
[b]玩家攻略查询[/b]：可搜索社区玩家贡献的攻略文档，补充实战经验。
[b]可切换人设[/b]：内置多种 AI 角色口吻（天道残魂、玩家怨念等），切换后下条消息生效。
[b]对话历史[/b]：对话自动保存，关闭面板不丢失，F8 重开可继续之前的话题。

[h2]使用前必读[/h2]

本 MOD [b]不内置模型[/b]，需要你自备兼容 OpenAI 接口格式的大模型 API。推荐 DeepSeek，其它支持 tool calling 的服务（通义千问、Kimi、GPT 等）也可使用。[b]API 费用由你自己的账户承担[/b]，Key 只保存在本地文件中，不会上传到任何第三方。

百晓册资料正在持续完善中，部分冷门内容可能存在滞后或缺失，敬请谅解。

